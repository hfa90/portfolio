create policy "profiles select own or admin" on public.profiles for select
  to authenticated using ((select auth.uid()) = id or public.is_admin());
create policy "profiles insert own" on public.profiles for insert
  to authenticated with check ((select auth.uid()) = id);
create policy "profiles update own or admin" on public.profiles for update
  to authenticated using ((select auth.uid()) = id or public.is_admin())
  with check ((select auth.uid()) = id or public.is_admin());

create policy "catalog public read" on public.produtos for select to anon, authenticated using (ativo = true or public.is_admin());
create policy "markets public read" on public.supermercados for select to anon, authenticated using (ativo = true or public.is_admin());
create policy "prices public read" on public.precos for select to anon, authenticated using (true);
create policy "catalog admin write" on public.produtos for all to authenticated using (public.is_admin()) with check (public.is_admin());
create policy "markets admin write" on public.supermercados for all to authenticated using (public.is_admin()) with check (public.is_admin());
create policy "prices admin write" on public.precos for all to authenticated using (public.is_admin()) with check (public.is_admin());

create policy "lists owner read" on public.listas for select to authenticated using ((select auth.uid()) = user_id);
create policy "lists owner insert" on public.listas for insert to authenticated with check ((select auth.uid()) = user_id);
create policy "lists owner delete" on public.listas for delete to authenticated using ((select auth.uid()) = user_id);
create policy "list items owner read" on public.lista_itens for select to authenticated
  using (exists (select 1 from public.listas l where l.id = lista_id and l.user_id = (select auth.uid())));
create policy "list items owner insert" on public.lista_itens for insert to authenticated
  with check (exists (select 1 from public.listas l where l.id = lista_id and l.user_id = (select auth.uid())));
create policy "list items owner delete" on public.lista_itens for delete to authenticated
  using (exists (select 1 from public.listas l where l.id = lista_id and l.user_id = (select auth.uid())));

create policy "notices public read" on public.avisos for select to anon, authenticated using (ativo = true or public.is_admin());
create policy "notices admin write" on public.avisos for all to authenticated using (public.is_admin()) with check (public.is_admin());

create policy "validations own insert" on public.validacoes_preco for insert to authenticated with check ((select auth.uid()) = user_id);
create policy "validations own/admin read" on public.validacoes_preco for select to authenticated using ((select auth.uid()) = user_id or public.is_admin());
create policy "validations admin update" on public.validacoes_preco for update to authenticated using (public.is_admin()) with check (public.is_admin());

create policy "vibe posts public read" on public.vibeconecta_posts for select to anon, authenticated using (true);
create policy "vibe posts owner insert" on public.vibeconecta_posts for insert to authenticated with check ((select auth.uid()) = user_id);
create policy "vibe posts owner delete" on public.vibeconecta_posts for delete to authenticated using ((select auth.uid()) = user_id or public.is_admin());
create policy "vibe comments public read" on public.vibeconecta_comentarios for select to anon, authenticated using (true);
create policy "vibe comments owner insert" on public.vibeconecta_comentarios for insert to authenticated with check ((select auth.uid()) = user_id);
create policy "vibe likes public read" on public.vibeconecta_likes for select to anon, authenticated using (true);
create policy "vibe likes owner insert" on public.vibeconecta_likes for insert to authenticated with check ((select auth.uid()) = user_id);
create policy "vibe likes owner delete" on public.vibeconecta_likes for delete to authenticated using ((select auth.uid()) = user_id);

create policy "finance owner read" on public.financas_transacoes for select to authenticated using ((select auth.uid()) = user_id);
create policy "finance owner insert" on public.financas_transacoes for insert to authenticated with check ((select auth.uid()) = user_id);
create policy "finance owner update" on public.financas_transacoes for update to authenticated using ((select auth.uid()) = user_id) with check ((select auth.uid()) = user_id);
create policy "finance owner delete" on public.financas_transacoes for delete to authenticated using ((select auth.uid()) = user_id);
create policy "goals owner read" on public.financas_metas for select to authenticated using ((select auth.uid()) = user_id);
create policy "goals owner write" on public.financas_metas for all to authenticated using ((select auth.uid()) = user_id) with check ((select auth.uid()) = user_id);

grant usage on schema public to anon, authenticated;
grant select on public.produtos, public.supermercados, public.precos, public.avisos, public.vibeconecta_posts, public.vibeconecta_comentarios, public.vibeconecta_likes to anon, authenticated;
grant select, insert, update, delete on all tables in schema public to authenticated;
grant usage, select on all sequences in schema public to authenticated;


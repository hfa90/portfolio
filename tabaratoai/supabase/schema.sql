-- Schema Supabase para o projeto Ta Barato Ai.
-- Rode este arquivo no SQL Editor do Supabase antes de publicar o frontend.
-- Depois configure static/js/supabase-config.js com a Project URL e a anon/publishable key.

create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nome text not null,
  email text unique,
  telefone text unique,
  endereco text,
  cep text,
  logradouro text,
  numero text,
  complemento text,
  bairro text,
  localidade text,
  uf text,
  bio text,
  foto_perfil text,
  pontos integer not null default 0,
  device_id text,
  role text not null default 'user',
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.supermercados (
  id bigint generated by default as identity primary key,
  nome text not null,
  rede text,
  codigo text,
  endereco text,
  cidade text default 'Manaus',
  estado text default 'AM',
  latitude numeric,
  longitude numeric,
  logo_url text,
  ativo boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.produtos (
  id bigint generated by default as identity primary key,
  nome text not null,
  categoria text,
  marca text,
  unidade text default 'un',
  codigo_barras text unique,
  imagem_url text,
  ativo boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.precos (
  id bigint generated by default as identity primary key,
  produto_id bigint not null references public.produtos(id) on delete cascade,
  supermercado_id bigint not null references public.supermercados(id) on delete cascade,
  preco numeric(10,2) not null check (preco >= 0),
  preco_anterior numeric(10,2),
  atualizado_em timestamptz not null default now(),
  unique (produto_id, supermercado_id)
);

create table if not exists public.listas (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  nome text not null default 'Minha lista',
  total numeric(12,2) not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.lista_itens (
  id bigint generated by default as identity primary key,
  lista_id bigint not null references public.listas(id) on delete cascade,
  produto_id bigint references public.produtos(id) on delete set null,
  nome text not null,
  quantidade numeric(10,2) not null default 1,
  preco numeric(10,2) not null default 0,
  mercado_nome text,
  imagem_url text
);

create table if not exists public.avisos (
  id bigint generated by default as identity primary key,
  titulo text not null,
  mensagem text not null,
  tipo text default 'info',
  ativo boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.validacoes_preco (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  preco_id bigint references public.precos(id) on delete cascade,
  tipo text not null,
  preco_sugerido numeric(10,2),
  foto_url text,
  status text not null default 'pendente',
  created_at timestamptz not null default now()
);

create table if not exists public.vibeconecta_posts (
  id bigint generated by default as identity primary key,
  user_id uuid references public.profiles(id) on delete set null,
  autor_nome text,
  texto text not null,
  supermercado text,
  imagem_url text,
  anonimato boolean not null default false,
  created_at timestamptz not null default now()
);

create table if not exists public.vibeconecta_comentarios (
  id bigint generated by default as identity primary key,
  post_id bigint not null references public.vibeconecta_posts(id) on delete cascade,
  user_id uuid references public.profiles(id) on delete set null,
  autor_nome text,
  texto text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.vibeconecta_likes (
  post_id bigint not null references public.vibeconecta_posts(id) on delete cascade,
  user_id uuid not null references public.profiles(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (post_id, user_id)
);

create table if not exists public.financas_transacoes (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  tipo text not null,
  categoria text,
  descricao text,
  valor numeric(12,2) not null default 0,
  data date not null default current_date,
  created_at timestamptz not null default now()
);

create table if not exists public.financas_metas (
  id bigint generated by default as identity primary key,
  user_id uuid not null references public.profiles(id) on delete cascade,
  categoria text not null,
  valor numeric(12,2) not null default 0,
  mes text,
  created_at timestamptz not null default now()
);

create or replace function public.is_admin()
returns boolean
language sql
stable
as $$
  select coalesce((auth.jwt() -> 'app_metadata' ->> 'role') = 'admin', false)
$$;

